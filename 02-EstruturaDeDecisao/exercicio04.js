// Fácil 4- Faça um programa que verifique (usando if e else) se uma letra é vogal ou consoante.

var letra = " ";

if (letra == "a" || letra == "e" || letra == "i" || letra == "o" || letra == "u") {
  console.log("Vogal");
} else if (letra == " ") {
  console.log("Nem vogal nem consoante");
} else {
  console.log("Consoante")
}
